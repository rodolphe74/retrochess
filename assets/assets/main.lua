function highlightSquare(i, cc, cf)
	local square_width = SQUARE_WIDTH_WIN
    local square_height = SQUARE_HEIGHT_WIN
	local xBoard = XBOARD_WIN	-- 9
	local yBoard = YBOARD_WIN	-- 10

	if osString == "Android" then
		square_width = SQUARE_WIDTH_ANDROID
		square_height = SQUARE_HEIGHT_ANDROID
		xBoard = XBOARD_ANDROID	-- 9
		yBoard = YBOARD_ANDROID	-- 10
	end
	
	local y = 7 - math.floor(i / 8)
	local x = math.floor(math.fmod(i, 8))
	
	if x > 7 or x < 0 then
		return -1
	end
	
	if y > 7 or y < 0 then
		return -1
	end
	
	local character = term:get_character(x * 3 + 10, y + 10)
	
	if (math.fmod(x + y, 2) == 0) then
		term:set_cursor_backcolor(cc)
	else
		term:set_cursor_backcolor(cf)
	end
	
	term:print(x * 3 + 10, y + 10, character)
	term:print(x * 3 + 9, y + 10, " ")
	term:print(x * 3 + 11, y + 10, " ")
end


