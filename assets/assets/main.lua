function putColorOnBoard(s, c, l)
	local col = c
	local fw = {1,1,1,1}
	local fb = {0,0,0,1}
	local bb = {0.3,0.3,0.3,1}
	local bw = {0.6,0.6,0.6,1}
	local flipflop = 0;
	for i = 1, #s do
		local chr = s:sub(i,i)
		if flipflop == 0 then
			term:set_cursor_backcolor(bw)
			flipflop = 1
		else 
			term:set_cursor_backcolor(bb)
			flipflop = 0
		end
		if chr:match("%W") then
			if string.byte(chr) == 10 then
				-- cr
				l = l + 1
				col = c
			elseif chr == "." then
				term:print(col, l, "   ")
				col = col + 3
			end
			
		else
			
		
			if(chr:match("%u")) then
				term:set_cursor_color(fw)
				term:print(col, l, " " .. chr .. " ")
				col = col + 3
			else
				term:set_cursor_color(fb)
				term:print(col, l, " " .. string.upper(chr) .. " ")
				col = col + 3
			end
		end
	end
	
	print("putColorOnBoard!")
end

function getSquareWin(xMouse, yMouse)
	local square_width = SQUARE_WIDTH_WIN
    local square_height = SQUARE_HEIGHT_WIN
	local xBoard = XBOARD_WIN	-- 9
	local yBoard = YBOARD_WIN	-- 10
	
	-- for y = 0, 7 do
		-- for x = 0, 7 do
			-- love.graphics.rectangle("line", xBoard + square_width * x, yBoard + square_height * y, 28, 25)
		-- end
	-- end
	
	local x = math.floor((xMouse - xBoard) / square_width)
	local y = math.floor((yMouse - yBoard) / square_height)
	
	if x > 7 or x < 0 then
		return -1
	end
	
	if y > 7 or y < 0 then
		return -1
	end
	
	local square = x + (7-y) * 8
	return square
end


