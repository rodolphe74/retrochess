function love.update(dt)
	if updateTimer(dt, "playWhite", timerList) then
		print("playWhite")
		deleteTimer("playWhite", timerList)
		local count, eval, from, to, t = l_alpha_beta(1, 5)	-- black search
		print(count, eval, from, to, t)
		l_move_to(from, to)	-- comit black play
		local board = l_get_board()	-- refresh board
		putColorOnBoard(board, 9, 10)
		
		checkWhite = l_is_check_mate(0)
		checkBlack = l_is_check_mate(1)
		
		print ("White is Mate " .. checkWhite)
		print ("Black is Mate " .. checkBlack)
	end
	
    term:update(dt)
end


function playWhite(from, to)
	l_move_to(from, to)	-- commit white play
	
	local board = l_get_board()	-- refresh board
	print(board)
	putColorOnBoard(board, 9, 10)
	
	addTimer(0.7, "playWhite", timerList)
end


function unselect()
	for k,v in pairs(lastSelection) do
		highlightSquare(v, {0.6,0.6,0.6,1}, {0.3,0.3,0.3,1})
	end
end


function love.mousepressed(x, y, button, istouch)
	if osString == "Android" then
		return
	end
	
	if button == 1 then
		local s = getSquareWin(x, y)
		print(s)
		local p = l_get_piece_at(s)	-- min black - maj white
		
		if p == "" or string.upper(p) ~= p then
			-- empty square ou piece adverse teste si le coup est possible
			for k,v in pairs(lastSelection) do
				if s == v then
					print("play:" .. lastSelection[1] .. "," .. s)
					playWhite(lastSelection[1], s)
					return
				end
			end
			unselect()	-- case vide ou la piece selectionnee ne peut aller
			return	
		end
		
		-- if string.upper(p) ~= p then
			-- unselect()	-- case noire
			-- return	
		-- end
	
		-- efface la selection precedent
		unselect()
		lastSelection = {}
		
		table.insert(lastSelection, s)	-- lastSelection[1] = piece selectionnee
		
		lst = l_get_moves_list(s)
		for k,v in pairs(lst) do
			highlightSquare(v, {0.3, 0.6, 0.3, 1}, {0.1, 0.6, 0.1, 1})
			table.insert(lastSelection, v)
		end
		highlightSquare(s, {0.3, 0.3, 0.6, 1}, {0.2, 0.2, 0.6, 1})
	end
end



function love.touchpressed(id, x, y, dx, dy, pressure)
	if osString ~= "Android" then
		return
	end
	
	local s = getSquareAndroid(x, y)
	local p = l_get_piece_at(s)	-- min black - maj white

	if p == "" then
		-- empty square test si le coup est possible
		for k,v in pairs(lastSelection) do
			if s == v then
				print("play")
				unselect()
				return
			end
		end
		unselect()	-- case vide ou la piece selectionnee ne peut aller
		return	
	end
	
	if string.upper(p) ~= p then
		unselect()	-- case noire
		return	
	end

	-- efface la selection precedente
	unselect()
	lastSelection = {}
	
	if string.upper(p) == p then
		-- human white - computer black
		table.insert(lastSelection, s)
		lst = l_get_moves_list(s)
		for k,v in pairs(lst) do
			highlightSquare(v, {0.3, 0.6, 0.3, 1}, {0.1, 0.6, 0.1, 1})
			table.insert(lastSelection, v)
		end
		highlightSquare(s, {0.3, 0.3, 0.6, 1}, {0.2, 0.2, 0.6, 1})
	end
end


function love.draw()
    love.graphics.clear()

    effect(function()
        love.graphics.push()
        love.graphics.scale(love.graphics.getWidth()/term.canvas:getWidth(), love.graphics.getHeight()/term.canvas:getHeight())
        love.graphics.rectangle("fill", 0, 0, term.canvas:getWidth(), term.canvas:getHeight())
		
        term:draw()
        love.graphics.pop()
    end)
end




function love.quit()
	readyToQuit = true
	if not readyToQuit then
		print("We are not ready to quit yet!")
		readyToQuit = true
		return true
	else
		print("Thanks for playing. Please play again soon!")
		l_close_chess_lib()
		return false
	end
end





