-- Operations bit a bit
local OR, XOR, AND = 1, 3, 4
function bit(u, l, oper)
   local r, m, s = 0, 2^31
   repeat
	  c,u,l = u+l+m, u%m, l%m
      r,m = r + m*oper%(c-u-l), m/2
   until m < 1
   return r
end


-- Affiche l'echiquier avec la font "AVENFONT.TTF"
function drawBoard(s, x, y)
	love.graphics.setFont(chessFont)
	local _x = x
	local _y = y
	local flipflop = 0;
	local count = 0;

	love.graphics.print("!\"\"\"\"\"\"\"\"#", _x, _y)
	_x = x 
	_y = _y + SQUARE_HEIGHT_CHESS_FONT
	for i = 1, #s do

		if count == 0 then
			love.graphics.print("$", _x, _y)
			_x = _x + SQUARE_WIDTH_CHESS_FONT
		end

		local chr = s:sub(i,i)

		if chr == "p" then
			if flipflop == 1 then
				love.graphics.print("p", _x, _y)
			else
				love.graphics.print("P", _x, _y)
			end
